num = str(input())

if num == "i" or num == "I":
    print("1")
elif num == "ii" or num == "II":
    print("2")
elif num == "iii" or num == "III":
    print("3")
elif num == "iv" or num == "IV":
    print("4")
elif num == "v" or num == "V":
    print("5")
elif num == "x" or num == "X":
    print("10")
elif num == "l" or num == "L":
    print("50")
elif num == "c" or num == "C":
    print("100")
elif num == "d" or num == "D":
    print("500")
elif num == "m" or num == "M":
    print("1000")